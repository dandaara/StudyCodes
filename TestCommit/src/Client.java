

public class Client {
	
	private int id;
	private int address;
	
	public Client(int address){
		this.address=(address);
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public int getAddress() {
		return address;
	}

	public void setAddress(int a) {
		this.address = a;
	}

	
	
	

}
